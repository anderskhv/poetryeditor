// Get today's rhyme based on day of year
export function getDailyRhyme(): RhymePair {
  const now = new Date();
  const start = new Date(now.getFullYear(), 0, 0);
  const diff = now.getTime() - start.getTime();
  const oneDay = 1000 * 60 * 60 * 24;
  const dayOfYear = Math.floor(diff / oneDay);

  // Use modulo to cycle through the year
  const index = (dayOfYear - 1) % dailyRhymes.length;
  return dailyRhymes[index];
}

// Get a specific day's rhyme (1-365)
export function getRhymeForDay(dayOfYear: number): RhymePair {
  const index = ((dayOfYear - 1) % dailyRhymes.length + dailyRhymes.length) % dailyRhymes.length;
  return dailyRhymes[index];
}
